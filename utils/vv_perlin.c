// Внутренняя функция получения значения градиента по таблице через координаты X, Y.
static uint32_t _get_table_hash( uint32_t x, uint32_t y ) {
	uint32_t y_value = perlin_hash_table[ ( y + PERLIN_OFFSET ) % 0x100 ];
	return perlin_hash_table[ ( y_value + x ) % 0x100 ];
}	// _get_table_hash

// Внутренняя функция линейной интерполяции.
static vv_fp_math_type _linear_interp( vv_fp_math_type x, vv_fp_math_type y, vv_fp_math_type t ) {
	// Функция: x * ( t - 1 ) + y * x.
	return x + ( y - x ) * t;	// После раскрытия скобок
}	// _linear_interp

// Внутренняя функция сглаженной интерполяции.
static vv_fp_math_type _smooth_interp( vv_fp_math_type x, vv_fp_math_type y, vv_fp_math_type t ) {
	return _linear_interp( x, y, t * t * ( 3 - 2 * t ) );
}	// _smooth_interp

static vv_fp_math_type _noise_2d( vv_fp_math_type x, vv_fp_math_type y ) {
	// Получение координат левой верхней вершины квадрата
	uint32_t rx = ( uint32_t )( x );
	uint32_t ry = ( uint32_t )( y );

	// Получение локальных координат внутри квадрата
	vv_fp_math_type x_frac = ( x - rx );
	vv_fp_math_type y_frac = ( y - ry );

	// Получение градиентных векторов текстуры(s - u, t - v) из квадрата
	uint32_t s = _get_table_hash( rx,		ry );
	uint32_t t = _get_table_hash( rx + 1,	ry );
	uint32_t u = _get_table_hash( rx,		ry + 1 );
	uint32_t v = _get_table_hash( rx + 1,	ry + 1 );

	// Сглаженная линейная интерполяция
	vv_fp_math_type low		= _smooth_interp( ( vv_fp_math_type )( s ), ( vv_fp_math_type )( t ), ( vv_fp_math_type )( x_frac ) );
	vv_fp_math_type high	= _smooth_interp( ( vv_fp_math_type )( u ), ( vv_fp_math_type )( v ), ( vv_fp_math_type )( x_frac ) );
	return _smooth_interp( low, high, y_frac );
}	// _noise_2d

vv_fp_math_type vv_perlin_2d( vv_fp_math_type x, vv_fp_math_type y, vv_fp_math_type frequency, int32_t depth ) {
	// Получение начальных значений X и Y
	vv_fp_math_type x_a = ( x * frequency );
	vv_fp_math_type y_a = ( y * frequency );

	// Инициализация
	vv_fp_math_type amplitude	= 1.0;
	vv_fp_math_type f_in		= 0.0;
	vv_fp_math_type div			= 0.0;

	// Цикл в глубину
	while ( depth > 0 ) {
		// Проход на шаг по глубине
		div += ( amplitude * 256 );
		f_in += ( _noise_2d( x_a, y_a ) * amplitude );
		amplitude /= 2;
		x_a *= 2;
		y_a *= 2;
		depth--;
	}	// while depth
	return ( f_in / div );
}	// vv_perlin_2d
